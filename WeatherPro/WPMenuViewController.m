#pragma mark -
#pragma mark Table View Delegate Methods

- (void)tableView:(UITableView *)tableView willDisplayCell:(UITableViewCell *)cell forRowAtIndexPath:(NSIndexPath *)indexPath
{
    cell.backgroundColor = [UIColor clearColor];
    cell.textLabel.textColor = [UIColor colorWithRed:62/255.0f green:68/255.0f blue:75/255.0f alpha:1.0f];
    cell.textLabel.font = [UIFont fontWithName:@"HelveticaNeue" size:17];
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
    if (indexPath.section == 0 && indexPath.row == 0) {
        WPViewController *wp = [[WPViewController alloc]init];
        WPNavigationController *nvai = [[WPNavigationController alloc]initWithRootViewController:wp];
        self.frostedViewController.contentViewController = nvai;
    }
    if (indexPath.section == 0) {
        switch (indexPath.row) {
            case 0:
            {
                WPStartViewController *start = [[WPStartViewController alloc]init];
                WPNavigationController *nvai = [[WPNavigationController alloc]initWithRootViewController:start];
                self.frostedViewController.contentViewController = nvai;
                break;

            }
            case 1:
            {
                WPViewController *weather = [[WPViewController alloc]init];
                WPNavigationController *nvai = [[WPNavigationController alloc]initWithRootViewController:weather];
                self.frostedViewController.contentViewController = nvai;
                break;
            }
            case 2:
            {
                WPMapViewController *map = [[WPMapViewController alloc]init];
                WPNavigationController *nvai = [[WPNavigationController alloc]initWithRootViewController:map];
                self.frostedViewController.contentViewController = nvai;
                break;
            }
            case 3:
            {
                WPAllListsViewController *list  = [[WPAllListsViewController alloc]initWithStyle:UITableViewStylePlain];
                WPNavigationController *nvai = [[WPNavigationController alloc]initWithRootViewController:list];
                self.frostedViewController.contentViewController = nvai;
                break;
            }
                
            default:
                break;
        }
    }
    [self.frostedViewController hideMenuViewController];
}


